package com.iangclifton.auid.customdrawable;

import android.app.Activity;
import android.content.res.Resources;
import android.graphics.Bitmap;
import android.graphics.BitmapFactory;
import android.graphics.drawable.Drawable;
import android.os.Bundle;
import android.view.Menu;
import android.widget.ImageView;

/**
 * Activity that demonstrates a custom {@link Drawable}.
 * 
 * @author Ian G. Clifton
 */
public class MainActivity extends Activity {

	@Override
	protected void onCreate(Bundle savedInstanceState) {
		super.onCreate(savedInstanceState);
		setContentView(R.layout.activity_main);
		
		// Create the custom Drawable
		final Resources res = getResources();
		final Bitmap b = BitmapFactory.decodeResource(res, R.drawable.photo);
		final int radius = res.getDimensionPixelSize(R.dimen.radius);
		final Drawable d = new RoundedBitmapDrawable(b, radius);
		
		// Find the ImageView and set the Drawable
		final ImageView iv = (ImageView) findViewById(R.id.image_view);
		iv.setImageDrawable(d);
	}

	@Override
	public boolean onCreateOptionsMenu(Menu menu) {
		// Inflate the menu; this adds items to the action bar if it is present.
		getMenuInflater().inflate(R.menu.activity_main, menu);
		return true;
	}

}
